package Homeworke;

public class FigurePainter2 {

    public static void main(String[] args) {
        for (int i = 0; i < 3; i++) {
            for (int j = 0; j < 1; j++) {
                System.out.println("       " + "*");
            }
            for (int k = 0; k < 4; k++) {
                System.out.print("* " + "* " +"* "+ "*");
            }
            System.out.println();

        }
    }
}





